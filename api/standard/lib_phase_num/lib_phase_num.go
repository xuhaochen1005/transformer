// CreatePhaseNum 添加相数规则
func CreatePhaseNum(c *gin.Context) {
	err := permission.Check(c, resource.Libraries, action.Write)
	if err != nil {
		c.Set("response", &model.Response{Code: http.StatusUnauthorized, Err: err})
		return
	}
	newPhaseNum := &model.PhaseNum{}
	err = errors.New(c.BindJSON(newPhaseNum), "请求数据有误，请联系开发人员进行处理")
	if err != nil {
		c.Set("response", &model.Response{Code: http.StatusBadRequest, Err: err})
		return
	}
	self, err := utils.GetUserFromRequest(c)
	if err != nil {
		c.Set("response", &model.Response{Code: http.StatusInternalServerError, Err: err})
		return
	}
	newPhaseNum.LpnCreator = self.ID
	err = errors.New(connector.DataSource.Create(newPhaseNum).Error, "相数记录信息添加失败，请稍后再试")
	if err != nil {
		c.Set("response", &model.Response{Code: http.StatusInternalServerError, Err: err})
		return
	}
	c.Set("response", &model.Response{Spec: "相数记录信息添加成功"})
}

// UpdatePhaseNum 更新相数规则
